        <p>來自台南善化的土地，許爸是位用心經營農場的農友，他有著對植物生長的敏感度，栽種過程中，他把每一株的蔬果當作自己的孩子，細心的呵護與經營，並且堅持著無農藥殘留與精緻耕作方法，呈現給消費者的是精緻、安心、健康自然原味的食材。</p>        <p>由於許爸精湛的農耕技術，讓附近的農友都會來農場觀摩；許爸輔導的農友，也替許爸取了『農業博士』的稱號，象徵許爸對農業創新、農友與社會的貢獻度。</p>        <p>小善心成立於2014年，成立的主因是來自於女兒對父親有著一層深厚的情感。女兒眼中的父親"許爸"多年來熱衷於所愛的土地農作上，這樣的熱忱及技術，並堅持著友善環境、善待作物的理念方式下耕作，並以創新的液態肥培技術導入作物，這樣的堅持讓七年級的女兒為之感動，希望透過產品作物將父親的理念傳遞讓更多人知道且能品嚐得到。所以將以品牌"小善心"創立來綿延倆代父女情感。</p>        <p>父親"許爸"耕作多年，這過程中熟識一群有著相同理念的農友合作組成農作平台，經由小善心源頭管理輔導方式，將食材建立生產紀錄，讓消費者透過平台更能清楚了解農友的耕作過程及產品資訊，以透明看得見方式來為消費者健康作把關。</p>
